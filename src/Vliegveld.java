public class Vliegveld {
    private String naam;
    private int tegenwind;
    private double landPrijs;

    public String getNaam() {
        return naam;
    }

    public void setNaam(String naam) {
        this.naam = naam;
    }

    public int getTegenwind() {
        return tegenwind;
    }

    public void setTegenwind(int tegenwind) {
        this.tegenwind = tegenwind;
    }

    public double getLandPrijs() {
        return landPrijs;
    }

    public void setLandPrijs(double landPrijs) {
        this.landPrijs = landPrijs;
    }
}